import Head from 'next/head'
import { useRouter } from 'next/router'
import React from 'react'
import SearchHeader from '../components/Header/SearchHeader'
import SearchResults from '../components/SearchResults'
import Response from '../Response'


const Search = ({data, query}) => {
  console.log(data)
  console.log(query)
  const router = useRouter()
   return (
    <div>
      {' '}
      <Head>
        <title> {router.query.term } - Google Search</title>{' '}
        <meta
          name='cloning google with nextjs'
          content='Generated by create next app'
        />
        <link rel='icon' href='/favicon.ico' />
      </Head>{' '}
      <SearchHeader />
      <SearchResults results={data}/>
    </div>
  )
}

export default Search



 export const   getServerSideProps = async (context) =>{
const useDummyData = false
const query = context.query.term
const  startIndex  =   context.query.start || '0'

const res = useDummyData ? Response : await fetch(
  `https://www.googleapis.com/customsearch/v1?key=${process.env.API_KEY}&cx=${process.env.CONTEXT_KEY}&q=${query}&start=${startIndex}`
)

const  data  = await  res.json()


return{
  props:{
    data:data,
    query:query
  }
}
 }